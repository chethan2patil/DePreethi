<h1>
The Primary School Grade 1 - 5</h1>
<p>As primary schooling is a time of multi-dimensional learning, we begin to introduce them progressively to the world of integrated knowledge, while particularly strengthening their aptitude in the core studies of English, Mathematics, General Science, and Social Science. </p>
<p>A wide range of classroom activities are used as stimuli for reading and writing. Appreciating the fact that computer literacy is essential in a modern world, our students are introduced to computers from class 1 to sharpen their analytical skills. Children also start using the Math Lab from Class 1. We encourage students in the primary school to do various projects in Environmental Science appreciating the cultural diversity, History and Geography of our planet. Physical training classes are conducted to develop their agility and fitness. Art and music classes create an awareness and appreciation of these creative forms </p>
<p>Establishing worthwhile work habits, persistence, independence, time management, and an appreciation for value of learning by oneself begins early in the schooling process atPreethi Public School. </p>
