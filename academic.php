<h1>
Kindergarten</h1>
<p>The Junior School has a dynamic environment in which leads to significant development in physical, emotional and intellectual growth. These are the 'Foundation Years' during which early linguistic skills, creative play, physical fitness and social skills are inculcated. This is the phase when a sense of identity takes shape. </p>
<p>Students of Kindergarten develop fine motor skills, auditory visual discrimination, hand-eye co-ordination, one-to-one correspondence and logical thinking. Activities are conducted to enhance children observation skills, quantitative analysis and sequencing. </p>
<p>In Kindergarten more emphasis is given on reading which helps to increase the vocabulary of students. Children start framing simple sentences, which brings out their creative writing skills. Mathematics and Science are dealt with interactive sessions where discussions from the child are encouraged and emphasized. Craft activities, storytelling, poetry recitation, singing, drama and dance, are a part of the Kindergarten programme. </p>
